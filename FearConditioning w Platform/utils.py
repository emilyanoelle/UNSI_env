def _normalize_key(v) -> str:
    if pd.isna(v):
        return ""
    return str(v).strip().upper()


def normalize_behavior_id_for_lookup(bid: str) -> Optional[str]:
    if bid is None:
        return None
    s = str(bid).strip().lower()
    s = s.strip("()[]{}\"'")
    s = re.sub(r"(_\d+(_fixed)?|_fixed)$", "", s, flags=re.IGNORECASE)
    s = s.strip(string.whitespace + string.punctuation.replace("-", "").replace("_", ""))
    return s


def extract_behavior_id_token(text: str) -> Optional[str]:
    if text is None:
        return None
    s = str(text).strip().strip("()[]{}\"'").lstrip("_- ")
    s = re.split(r"[_\s\(\)\[\]\{\}]+", s, maxsplit=1)[0].strip()
    if not s:
        return None
    for pat in [r"^([0-9]+[A-Za-z]?(?:-[0-9A-Za-z]+)?)",
                r"^([A-Za-z]?[0-9]+[A-Za-z]?(?:-[0-9A-Za-z]+)?)"]:
        m = re.match(pat, s)
        if m:
            return m.group(1)
    return s


def parse_filename_bits(csv_path: Path) -> tuple:
    stem = csv_path.stem
    m    = re.search(r"(\d{2}-\d{2}-\d{2})", stem)
    if not m:
        return None, None
    date = m.group(1)
    rest = stem[m.end():].lstrip("_ -")
    if not rest:
        return date, None
    bid = extract_behavior_id_token(rest)
    return date, bid


def find_metadata_for_behavior(meta_df: pd.DataFrame, raw_bid: str) -> pd.DataFrame:
    if raw_bid is None or meta_df is None:
        return pd.DataFrame()

    bid_raw = str(raw_bid)
    if "_" in bid_raw:
        bid_raw = bid_raw.split("_", 1)[0]

    bid_norm = normalize_behavior_id_for_lookup(bid_raw)
    if not bid_norm:
        return pd.DataFrame()

    meta_beh   = meta_df["behavior_id"].astype(str)
    meta_norm  = meta_beh.str.lower().str.strip()
    meta_clean = meta_norm.str.replace(r"[^a-z0-9]", "", regex=True)

    mask = meta_norm == bid_norm
    if mask.any():
        return meta_df[mask]

    cleaned = re.sub(r"[^a-z0-9]", "", bid_norm)
    if cleaned:
        mask = meta_clean == cleaned
        if mask.any():
            return meta_df[mask]

    m = re.match(r"(.+?)(-\d+|-[a-z0-9]+)$", bid_norm)
    if m:
        mask = meta_norm == m.group(1)
        if mask.any():
            return meta_df[mask]

    mnum = re.match(r"^0*([0-9]+)([a-z])?$", bid_norm)
    if mnum:
        num, letter = mnum.group(1), (mnum.group(2) or "").lower()
        for suffix in ([f"-{num}", num] + ([f"{num}{letter}", ] if letter else [])):
            mask = meta_norm.str.endswith(suffix)
            if mask.any():
                return meta_df[mask]
        if letter:
            mask = meta_clean.str.endswith(f"{num}{letter}")
            if mask.any():
                return meta_df[mask]

    pat  = re.compile(r"(^|[-_])" + re.escape(bid_norm) + r"($|[-_])")
    mask = meta_norm.apply(lambda s: bool(pat.search(s)))
    if mask.any():
        return meta_df[mask]

    if cleaned:
        for fn in [meta_clean.str.startswith, meta_clean.str.endswith]:
            mask = fn(cleaned)
            if mask.any():
                return meta_df[mask]

    return pd.DataFrame()


