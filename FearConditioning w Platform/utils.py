def normalize_trial_type(v: str) -> str:
    if not isinstance(v, str):
        return "unknown"
    s = v.strip().lower().replace("_", "")
    if s in {"cs+", "csplus", "csplus1", "csp"}:
        return "CS+"
    if s in {"cs-", "csminus", "csminus1", "csm"}:
        return "CS-"
    if s in {"iti", "intertrialinterval", "inter_trial_interval"}:
        return "ITI"
    return "unknown"


def rising_edges(series: pd.Series) -> np.ndarray:
    s    = series.fillna(0).astype(float)
    prev = s.shift(1, fill_value=0)
    return ((prev <= 0) & (s > 0)).to_numpy().nonzero()[0]


def get_off_time(df: pd.DataFrame, time_col: str, off_col: Optional[str],
                 start_time: float, default_len: float) -> float:
    if off_col is None or off_col not in df.columns:
        return start_time + default_len
    sub = df[df[time_col] >= start_time]
    idx = sub[off_col].fillna(0).astype(float).to_numpy().nonzero()[0]
    if len(idx) == 0:
        return start_time + default_len
    return float(df.loc[sub.index[idx[0]], time_col])


def clip_interval(start: float, end: float, t_min: float, t_max: float) -> tuple:
    return max(start, t_min), min(end, t_max)


def detect_trials_from_ttl(df, time_col, csplus_on, csplus_off,
                            csminus_on, csminus_off,
                            trial_len_s=30.0, iti_len_s=60.0) -> tuple:
    t = df[time_col].astype(float).to_numpy()
    t_min, t_max = float(np.nanmin(t)), float(np.nanmax(t))
    trials = []

    for on_col, off_col, label in [
        (csplus_on, csplus_off, "CS+"),
        (csminus_on, csminus_off, "CS-"),
    ]:
        if on_col and on_col in df.columns:
            for ix in rising_edges(df[on_col]):
                start = float(df.iloc[ix][time_col])
                end   = get_off_time(df, time_col, off_col, start, trial_len_s)
                start, end = clip_interval(start, end, t_min, t_max)
                if end > start:
                    trials.append(dict(type=label, start=start, end=end))

    trials.sort(key=lambda d: d["start"])

    itis = []
    for tr in trials:
        s, e = clip_interval(tr["end"], tr["end"] + iti_len_s, t_min, t_max)
        if e > s:
            itis.append(dict(type="ITI", start=s, end=e))

    counts = {"CS+": 0, "CS-": 0, "ITI": 0}
    for w in trials + itis:
        counts[w["type"]] += 1
        w["trial_index"] = counts[w["type"]]

    return trials, itis


def detect_trials_from_tone_status(df, time_col, csplus_col, csminus_col,
                                    iti_len_s=60.0) -> tuple:
    t = df[time_col].astype(float).to_numpy()
    t_min, t_max = float(np.nanmin(t)), float(np.nanmax(t))
    trials = []

    for col, label in [(csplus_col, "CS+"), (csminus_col, "CS-")]:
        s    = df[col].fillna(0).astype(float).to_numpy()
        prev = np.roll(s, 1); prev[0] = 0
        starts = np.where((prev <= 0) & (s > 0))[0]
        ends   = np.where((prev > 0) & (s <= 0))[0]
        if len(starts) > len(ends):
            ends = np.append(ends, len(s) - 1)
        for i, si in enumerate(starts):
            if i >= len(ends):
                break
            st, en = clip_interval(float(t[si]), float(t[ends[i]]), t_min, t_max)
            if en > st:
                trials.append(dict(type=label, start=st, end=en))

    trials.sort(key=lambda d: d["start"])

    itis = []
    for tr in trials:
        s, e = clip_interval(tr["end"], tr["end"] + iti_len_s, t_min, t_max)
        if e > s:
            itis.append(dict(type="ITI", start=s, end=e))

    counts = {"CS+": 0, "CS-": 0, "ITI": 0}
    for w in trials + itis:
        counts[w["type"]] += 1
        w["trial_index"] = counts[w["type"]]

    return trials, itis


def detect_trials(df: pd.DataFrame, time_col: str, cfg: dict) -> tuple:
    mode            = cfg.get("cs_detection_mode", "auto")
    csplus_pattern  = cfg.get("tone_status_col_csplus",  "cs plus tone status")
    csminus_pattern = cfg.get("tone_status_col_csminus", "cs minus tone status")

    def _try_tone_status():
        csplus_col, csminus_col = find_tone_status_cols(df, csplus_pattern, csminus_pattern)
        trials, itis = detect_trials_from_tone_status(df, time_col, csplus_col, csminus_col)
        return trials, itis, f"tone_status ({csplus_col}, {csminus_col})"

    def _try_ttl():
        csplus_on, csplus_off, csminus_on, csminus_off = find_ttl_cols(df)
        trials, itis = detect_trials_from_ttl(
            df, time_col, csplus_on, csplus_off, csminus_on, csminus_off)
        return trials, itis, f"TTL ({csplus_on}, {csminus_on})"

    if mode == "tone_status":
        return _try_tone_status()
    if mode == "ttl":
        return _try_ttl()
    try:
        return _try_tone_status()
    except ValueError:
        return _try_ttl()


def filter_trials(df: pd.DataFrame, cs_cap: int = 10, iti_cap: int = 20) -> pd.DataFrame:
    if "_trial_type" not in df.columns and "trial_type" in df.columns:
        df = df.copy()
        df["_trial_type"] = df["trial_type"]

    if "trial_index" not in df.columns:
        df = df.sort_values(["_day_folder", "treatment_group", "_trial_type", "_source_csv"])
        df["trial_index"] = (
            df.groupby(["_day_folder", "treatment_group", "_trial_type", "_source_csv"])
              .cumcount() + 1
        )

    capped = []
    for label, sub in df.groupby("_trial_type", dropna=False):
        if label in ("CS+", "CS-"):
            capped.append(sub[sub["trial_index"] <= cs_cap])
        elif label == "ITI":
            capped.append(sub[sub["trial_index"] <= iti_cap])
        else:
            capped.append(sub)
    return pd.concat(capped, ignore_index=True) if capped else df


# =============================================================================
# Signal integration
# =============================================================================

def integrate_binary(t, x, start, end) -> float:
    t = np.asarray(t, dtype=float)
    x = np.asarray(x, dtype=float)
    if len(t) < 2 or end <= start:
        return 0.0
    dt      = np.diff(t)
    dt      = np.append(dt, dt[-1])
    left    = np.maximum(t, start)
    right   = np.minimum(t + dt, end)
    overlap = np.maximum(0.0, right - left)
    return float((overlap * (x > 0).astype(float)).sum())


def interval_fraction_on(t, x, start, end) -> float:
    denom = integrate_binary(t, np.ones_like(x), start, end)
    if denom < 1e-12:
        return 0.0
    return integrate_binary(t, x, start, end) / denom


